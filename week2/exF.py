import turtle as t  #相当于typedef
t.setup(960,640)

t.pensize(200)
t.pencolor("yellow")
t.circle(100,360)

t.penup()
t.seth(90)
t.fd(90)
t.seth(180)
t.fd(150)
t.seth(270)
t.pendown()
t.pensize(5)
t.pencolor("black")
t.circle(150,180)

t.penup()
t.fd(90)
t.seth(155)
t.pendown()
t.pensize(40)
t.pencolor("white")
t.circle(160,45)
t.penup()
t.circle(160,-45)
t.seth(180)
t.fd(180)
t.seth(155)
t.pendown()
t.circle(160,45)

t.penup()
t.seth(0)
t.fd(20)
t.pendown()
t.pensize(16)
t.pencolor("black")
t.circle(8)
t.penup()
t.fd(180)
t.pendown()
t.circle(8)
t.done()